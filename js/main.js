let currentLang = 'id';

function applyLang(lang) {
    const data = translations[lang];
    document.querySelectorAll('[data-id]').forEach(function(el) {
        const key = el.getAttribute('data-id');
        if (data[key] !== undefined) {
            el.innerHTML = data[key];
        }
    });
    document.getElementById('langToggle').textContent = lang === 'id' ? 'EN' : 'ID';
    document.documentElement.lang = lang;

    const waBase = 'https://wa.me/628190635550?text=';
    const waMsg = lang === 'id'
        ? 'Halo%20Psygnosis%20Solution%2C%20saya%20tertarik%20dengan%20layanan%20kalian.'
        : 'Hi%20Psygnosis%20Solution%2C%20I%27m%20interested%20in%20your%20services.';
    document.querySelectorAll('a[href*="wa.me"]').forEach(function(el) {
        el.href = waBase + waMsg;
    });
}

document.getElementById('langToggle').addEventListener('click', function() {
    currentLang = currentLang === 'id' ? 'en' : 'id';
    applyLang(currentLang);
});